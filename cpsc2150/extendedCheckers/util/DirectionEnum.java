package cpsc2150.extendedCheckers.util;

/**
 * Enumeration that provides player with four cardinal diagonal moving directions during a checkers game
 *
 * @pre None
 *
 * @post
 * DirectionEnum.NE = [Northeast direction] AND
 * DirectionEnum.NW = [Northwest direction] AND
 * DirectionEnum.SE = [Southeast direction] AND
 * DirectionEnum.SW = [Southwest direction]
 *
 */

public enum DirectionEnum
{
    NE,
    NW,
    SE,
    SW
}
